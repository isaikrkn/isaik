function applyLanguage(lang) {
  const safeLang = content[lang] ? lang : 'en';
  const pack = content[safeLang];

  document.documentElement.lang = safeLang;
  document.title = pack.meta.title;
  if (metaDescription) metaDescription.setAttribute('content', pack.meta.description);

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const path = node.dataset.i18n.split('.');
    let value = pack;
    for (const step of path) value = value?.[step];
    if (typeof value === 'string') node.textContent = value;
  });

  renderProjects(safeLang);
  renderPortfolio(safeLang);
  renderServices(safeLang);

  langSwitcher.querySelectorAll('.lang-btn').forEach((button) => {
    const isActive = button.dataset.lang === safeLang;
    button.classList.toggle('active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });

  localStorage.setItem('isaik-language', safeLang);
}

if (toggle && nav) {
  toggle.addEventListener('click', () => {
    const expanded = toggle.getAttribute('aria-expanded') === 'true';
    toggle.setAttribute('aria-expanded', String(!expanded));
    nav.classList.toggle('nav-open');
  });

  nav.querySelectorAll('a').forEach((link) => {
    link.addEventListener('click', () => {
      nav.classList.remove('nav-open');
      toggle.setAttribute('aria-expanded', 'false');
    });
  });
}

langSwitcher.querySelectorAll('.lang-btn').forEach((button) => {
  button.addEventListener('click', () => applyLanguage(button.dataset.lang));
});

applyLanguage(localStorage.getItem('isaik-language') || 'en');
